package yourpck.retrofit2.converter.gson;

import yourpck.com.gson.Gson;
import yourpck.com.gson.JsonIOException;
import yourpck.com.gson.TypeAdapter;
import yourpck.com.gson.stream.JsonReader;
import yourpck.com.gson.stream.JsonToken;

import java.io.IOException;

import yourpck.okhttp3.ResponseBody;
import yourpck.retrofit2.Converter;

final class GsonResponseBodyConverter<T> implements Converter<ResponseBody, T> {
    private final Gson gson;
    private final TypeAdapter<T> adapter;

    GsonResponseBodyConverter(Gson gson, TypeAdapter<T> adapter) {
        this.gson = gson;
        this.adapter = adapter;
    }

    @Override
    public T convert(ResponseBody value) throws IOException {
        JsonReader jsonReader = gson.newJsonReader(value.charStream());
        try {
            T result = adapter.read(jsonReader);
            if (jsonReader.peek() != JsonToken.END_DOCUMENT) {
                throw new JsonIOException("JSON document was not fully consumed.");
            }
            return result;
        } finally {
            value.close();
        }
    }
}
